    private void ButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonAjouterActionPerformed
        // TODO add your handling code here:
        String type = "";
        if(RadioFemme.isSelected())
        {
            type = RadioFemme.getText();
        }
        if(RadioHomme.isSelected())
        {
            type = RadioHomme.getText();
        }
        String qt_string = SpinnerQuant.getValue().toString();
        int qt = Integer.parseInt(qt_string);
        
        DepotDAO depotDAO = new DepotDAO();
        Depot d = depotDAO.findDepotByAdresse(ComboBoxDepot.getSelectedItem().toString());
        Stock stock = new Stock();
        stock.setDepot(d);
        stock.setNombre_articles(qt);
        stock.setType_vet_stock(type);
        StockDAO stockDAO = new StockDAO();
        stockDAO.insertStock(stock);
        JOptionPane.showMessageDialog(this, "Jawek 7af√©li!!");
    }//GEN-LAST:event_ButtonAjouterActionPerformed

